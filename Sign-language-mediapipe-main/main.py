    cap = cv2.VideoCapture(args.device)
    cvFpsCalc = CvFpsCalc(buffer_len=10)
    accumulated_gestures = []  # List to store gesture texts
    last_gesture_time = time.time()
    gesture_cooldown = 1.0  # 1 second cooldown between gestures

    keypoint_classifier = KeyPointClassifier()
    point_history_classifier = PointHistoryClassifier()
    with open('model/keypoint_classifier/keypoint_classifier_label.csv',
              encoding='utf-8-sig') as f:
        keypoint_classifier_labels = csv.reader(f)
        keypoint_classifier_labels = [
            row[0] for row in keypoint_classifier_labels
        ]

    with open(
            'model/point_history_classifier/point_history_classifier_label.csv',
            encoding='utf-8-sig') as f:
        point_history_classifier_labels = csv.reader(f)
        point_history_classifier_labels = [
            row[0] for row in point_history_classifier_labels
        ]

    # Coordinate history #################################################################
    history_length = 16
    accumulated_text = ""
    last_update_time = time.time()

    point_history = deque(maxlen=history_length)
    finger_gesture_history = deque(maxlen=history_length)

    with mp_hands.Hands(
            static_image_mode=use_static_image_mode,
            max_num_hands=2,
            model_complexity=0,
            min_detection_confidence=0.75,
            min_tracking_confidence=0.5) as hands:


        while cap.isOpened():
            fps = cvFpsCalc.get()
            success, image = cap.read()
            if not success:
                print("Ignoring empty camera frame.")
                # If loading a video, use 'break' instead of 'continue'.
                continue

            # To improve performance, optionally mark the image as not writeable to
            # pass by reference.
            image.flags.writeable = False
            image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
            results = hands.process(image)

            # Draw the hand annotations on the image.
            image.flags.writeable = True
            image = cv2.cvtColor(image, cv2.COLOR_RGB2BGR)
            gesture_detected = None
            if results.multi_hand_landmarks is not None:
                for hand_landmarks, handedness in zip(results.multi_hand_landmarks,results.multi_handedness):

                    mp_drawing.draw_landmarks(
                        image,
                        hand_landmarks,
                        mp_hands.HAND_CONNECTIONS,
                        mp_drawing_styles.get_default_hand_landmarks_style(),
                        mp_drawing_styles.get_default_hand_connections_style())



                    landmark_list = calc_landmark_list(image, hand_landmarks)
                    pre_processed_landmark_list = pre_process_landmark(landmark_list)
                    pre_processed_point_history_list = pre_process_point_history(image, point_history)



                    confidence, hand_sign_id = keypoint_classifier(pre_processed_landmark_list)

                    if hand_sign_id == 1:
                        point_history.append(landmark_list[8])
                    else:
                        point_history.append([0,0])

                    if (confidence > 0.75):
                        info_text = keypoint_classifier_labels[hand_sign_id-1]
                    else:
                        info_text = None

                    # Finger gesture classification
                    finger_gesture_id = 0
                    point_history_len = len(pre_processed_point_history_list)
                    if point_history_len == (history_length * 2):
                        finger_gesture_id = point_history_classifier(pre_processed_point_history_list)

                    finger_gesture_history.append(finger_gesture_id)
                    most_common_fg_id = Counter(finger_gesture_history).most_common()

                    image = draw_info(image, hand_landmarks, info_text)
                    image = draw_landmarks(image, landmark_list)
                    image = draw_info_text(
                        image,point_history_classifier_labels[most_common_fg_id[0][0]] )






                    #print(info_text)
                    #accumulation
                    gesture_detected = info_text
                    if gesture_detected and time.time() - last_gesture_time > gesture_cooldown:
                        accumulated_gestures.append(gesture_detected)
                        last_gesture_time = time.time()







            else:
                point_history.append([0,0])


            new_frame_time = time.time()

            image = draw_point_history(image, point_history)
            # Flip the image horizontally for a selfie-view display.
            image = cv2.flip(image, 1)

            key = cv2.waitKey(1)
            if key == 32:  # Space key
                accumulated_gestures.append('')
            elif key == ord('d'):  # 'd' key
                accumulated_gestures.pop()
            elif key == ord('q'):
                break




                # Display the accumulated gestures
            display_text = ' '.join(filter(None,accumulated_gestures)).strip()
            text_position = (10, image.shape[0] - 10)

            cv2.putText(image, "FPS:" + str(fps), (7, 30), cv2.FONT_HERSHEY_SIMPLEX, 1, (255, 255, 0), 3, cv2.LINE_AA)
            cv2.putText(image, display_text, text_position, cv2.FONT_HERSHEY_SIMPLEX, 1, (0, 255, 0), 2)


            cv2.imshow('SLRS', image)
            if cv2.waitKey(10) & 0xFF == ord('q'):
                break
    cap.release()
    cv2.destroyAllWindows()





